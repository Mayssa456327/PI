    public function findTopLikedComments(): array
    {
        $qb = $this->createQueryBuilder('c')
            ->select('c', 'b', 'COUNT(c.id) AS totalComments')
            ->join('c.blog', 'b')
            ->groupBy('c.blog')
            ->orderBy('totalComments', 'DESC')
            ->setMaxResults(10); // Adjust the number of top liked comments as needed

        return $qb->getQuery()->getResult();
    }
